#Operadores aritméticos

x = 16
y = 4

#Adição
somando = x + y
print("resultado da soma : ",somando)
print(type(somando))

#Subtração
subtraindo = x - y
print("resultado da subtração: ",subtraindo)
print(type(subtraindo))

#Multiplicação
multiplicando = x * y
print("resultado da multiplicação : ",multiplicando)
print(type(multiplicando))

#Divisão
dividindo = x / y
print("resultado da divisão : ",dividindo)
print(type(dividindo))

#Divisão completa
dividindo1 = x // y
print("resultado da divisão completa : ",dividindo1)
print(type(dividindo1))

#Potência
potenciando = x ** y
print("resultado da potenciação : ",potenciando)
print(type(potenciando))

#Módulo
modulando = x % y 
print("resultado do módulo : ",modulando)
print(type(modulando))


